fun main(args: Array<String>) {
    var a = 10
    var b = 9
    println(a==b)// a.equals(b)
    println(a!=b)// !a.equals(b)
    println(a>b)// a.compareTo(b)>0
    println(a<b)// a.compareTo(b)<0
    println(a>=b)// a.comperaTo(b)>=0
    println(a<=b)// a.comperaTo(b)<=0
    println(a.equals(b))
    println(!a.equals(b))
    println(a.compareTo(b))
    println(a)
}